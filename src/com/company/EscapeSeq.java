package com.company;

public class EscapeSeq {

    public static void main(String[] args) {
	// Print a box in Java
        System.out.println("*********");
        System.out.println("*\t\t*");
        System.out.println("*\t\t*");
        System.out.println("*\t\t*");
        System.out.println("*\t\t*");
        System.out.println("*********");

        //Insert New Line
        System.out.println("\n");

        //Print a second identical box
        System.out.println("*********\n*\t\t*\n*\t\t*\n*\t\t*\n*\t\t*\n*********");

        //Insert New Line
        System.out.println("\n");

        //Print an Oval
        System.out.println("   ***");
        System.out.println(" *\t   *");
        System.out.println("*\t\t*");
        System.out.println("*\t\t*");
        System.out.println(" *\t   *");
        System.out.println("   ***");
    }
}
